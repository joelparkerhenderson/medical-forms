<div class="space-y-6 text-gray-700 leading-relaxed">
	<section>
		<h2 class="text-xl font-bold text-primary mb-3">
			How your information is shared so that this practice can meet legal requirements
		</h2>
		<p class="mb-3">
			The law requires this practice to share information from your medical records in certain
			circumstances. Information is shared so that the NHS or the UK Health Security Agency can,
			for example:
		</p>
		<ul class="list-disc pl-6 mb-3 space-y-1">
			<li>plan and manage services;</li>
			<li>check that the care being provided is safe;</li>
			<li>prevent infectious diseases from spreading.</li>
		</ul>
		<p class="mb-3">
			We will share information with NHS England, the Care Quality Commission, and local health
			protection team (or UK Health Security Agency) when the law requires us to do so. We must
			also share your information if a court of law orders us to do so.
		</p>
	</section>

	<section>
		<h3 class="text-lg font-semibold text-primary mb-2">NHS England</h3>
		<ul class="list-disc pl-6 space-y-1">
			<li>
				NHS England is a national body which has legal responsibilities to collect information
				about health and social care services.
			</li>
			<li>
				It collects information from across the NHS in England and provides reports on how the
				NHS is performing. These reports help to plan and improve services to patients.
			</li>
			<li>
				This practice must comply with the law and will send data to NHS England, for example,
				when it is told to do so by the Secretary of State for Health under the Health and Social
				Care Act 2012.
			</li>
		</ul>
	</section>

	<section>
		<h3 class="text-lg font-semibold text-primary mb-2">Care Quality Commission (CQC)</h3>
		<ul class="list-disc pl-6 space-y-1">
			<li>
				The CQC regulates health and social care services to ensure that safe care is provided.
			</li>
			<li>
				The law says that we must report certain serious events to the CQC, for example, when
				patient safety has been put at risk.
			</li>
		</ul>
	</section>

	<section>
		<h3 class="text-lg font-semibold text-primary mb-2">Public Health</h3>
		<ul class="list-disc pl-6 space-y-1">
			<li>
				The law requires us to share data for public health reasons, for example to prevent the
				spread of infectious diseases or other diseases which threaten the health of the
				population.
			</li>
			<li>
				We will report the relevant information to local health protection team or the UK Health
				Security Agency.
			</li>
		</ul>
	</section>

	<section class="bg-light-gray p-4 rounded-lg border border-border">
		<h3 class="text-lg font-semibold text-primary mb-2">Legal basis for processing</h3>
		<p class="mb-2">We are required by law to provide you with the following information:</p>

		<p class="mb-1"><strong>Purpose of the processing:</strong> Compliance with legal obligations or court order.</p>

		<p class="mb-1"><strong>Lawful basis for processing:</strong></p>
		<ul class="list-disc pl-6 mb-3 space-y-1">
			<li>
				<strong>Article 6(1)(c)</strong> — processing is necessary for compliance with a legal
				obligation to which the controller is subject.
			</li>
			<li>
				<strong>Article 9(2)(h)</strong> — processing is necessary for the purpose of
				preventative medicine, the provision of health or social care or treatment or the
				management of health or social care systems and services.
			</li>
		</ul>

		<p class="mb-1"><strong>Recipients of the processed data:</strong></p>
		<ul class="list-disc pl-6 mb-3 space-y-1">
			<li>NHS England</li>
			<li>The Care Quality Commission</li>
			<li>Local health protection team or the UK Health Security Agency</li>
			<li>A court if ordered to do so</li>
		</ul>
	</section>

	<section>
		<h3 class="text-lg font-semibold text-primary mb-2">Right to object and the National Data Opt-out</h3>
		<p class="mb-2">
			There are very limited rights to object when the law requires information to be shared.
			The National Data Opt-out does not apply when there is a legal requirement to share
			confidential information.
		</p>
		<p class="mb-1">You are unable to object when information is legally required:</p>
		<ul class="list-disc pl-6 space-y-1">
			<li>under public health legislation</li>
			<li>if the Care Quality Commission needs information for their regulatory functions</li>
			<li>by a court</li>
		</ul>
	</section>

	<section>
		<h3 class="text-lg font-semibold text-primary mb-2">Your rights</h3>
		<ul class="list-disc pl-6 space-y-1">
			<li>You have the right to access your medical record.</li>
			<li>You have the right to correct personal information which is inaccurate.</li>
			<li>
				You have the right to ask us to restrict the processing of your personal information in
				certain circumstances.
			</li>
			<li>
				GP medical records will be kept in line with the law and national guidance.
			</li>
			<li>
				You have the right to complain to the Information Commissioner's Office (ICO):
				<a href="https://ico.org.uk/global/contact-us/" class="text-primary underline" target="_blank" rel="noopener">
					https://ico.org.uk/global/contact-us/
				</a>
				or call 0303 123 1113.
			</li>
		</ul>
	</section>
</div>
