<SectionCard title="Screening Program Privacy Notice" description="How your information is used in NHS national screening programmes to identify early signs of disease or risk.">
	<div class="prose prose-sm max-w-none text-gray-800">
		<p>
			The NHS runs national screening programmes to look for early signs of disease, or to identify people at higher risk, so that earlier treatment can be offered. Screening is offered to eligible people at specific ages or in specific situations.
		</p>

		<h3 class="mt-4 text-lg font-semibold">Programmes covered</h3>
		<ul class="list-disc space-y-2 pl-5">
			<li><strong>Cervical screening</strong> — women aged 25-64</li>
			<li><strong>Breast screening</strong> — women aged 50-71</li>
			<li><strong>Bowel cancer screening</strong> — people aged 50-74</li>
			<li><strong>Abdominal aortic aneurysm screening</strong> — men aged 65</li>
			<li><strong>Diabetic eye screening</strong> — people with diabetes aged 12+</li>
			<li><strong>Antenatal and newborn screening</strong> — during pregnancy and shortly after birth</li>
		</ul>

		<h3 class="mt-4 text-lg font-semibold">What information is used</h3>
		<p>
			Your GP practice, NHS England, and your local screening provider share the information required to identify when you are eligible for screening, to invite you, to carry out the screening, and to record and act on the results. This includes your NHS number, contact details, date of birth, and relevant medical history.
		</p>

		<h3 class="mt-4 text-lg font-semibold">Legal bases</h3>
		<p>
			Processing is lawful under <strong>UK GDPR Article 6(1)(e)</strong> (task in the public interest) and, for health data, <strong>Article 9(2)(h)</strong> (provision of health care) and <strong>Article 9(2)(i)</strong> (public interest in public health). NHS England is the joint data controller with your GP practice for the national programmes.
		</p>

		<h3 class="mt-4 text-lg font-semibold">Your choices</h3>
		<p>
			You can ask to be withdrawn from individual screening programmes at any time by contacting your GP practice or NHS England. Withdrawing from screening does not affect other healthcare you receive. You can change your mind at any time.
		</p>

		<h3 class="mt-4 text-lg font-semibold">Your rights</h3>
		<p>
			You have rights to access, correct, and (in some cases) object to or erase your personal data. You can complain to the Information Commissioner's Office at
			<a class="text-primary underline" href="https://ico.org.uk/" target="_blank" rel="noopener">ico.org.uk</a>.
		</p>

		<p class="mt-6 text-sm text-gray-600">
			Please read this notice carefully before proceeding to acknowledge and sign.
		</p>
	</div>
