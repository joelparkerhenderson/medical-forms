#!/usr/bin/env python3
"""Combine each form's numbered SQL migrations into one sql-migrations/schema.sql file.

For each form with a `sql-migrations/` directory, concatenates every
`NN-*.sql` migration file (in numeric order) into a single `schema.sql`
file at `forms/<slug>/sql-migrations/schema.sql`. The combined file is
a convenience rollup of the whole schema suitable for one-shot apply in
development databases, editor inspection, or LLM context windows.

The generator only consumes files whose name begins with a digit; it
ignores `schema.sql` itself so re-running is idempotent.
"""

import sys
from pathlib import Path

TOP = Path(__file__).resolve().parent.parent
FORMS_DIR = TOP / "forms"

COMBINED_NAME = "schema.sql"


def numbered_sql_files(sql_dir: Path):
    """Return numbered migration files (e.g. 00-extensions.sql) sorted by name."""
    return sorted(f for f in sql_dir.glob("*.sql") if f.name[:1].isdigit())


def render_combined(form_slug: str, files) -> str:
    """Render a combined schema.sql content for one form."""
    header = [
        f"-- {COMBINED_NAME}",
        f"-- Combined schema for form: {form_slug}",
        "--",
        f"-- Auto-generated by bin/generate-sql-combined.py from the numbered",
        f"-- migration files in this directory. Do not edit by hand — re-run",
        f"-- the generator after changing any NN-*.sql file.",
        "--",
        f"-- Source files ({len(files)}):",
    ]
    for f in files:
        header.append(f"--   - {f.name}")
    header.append("")
    parts = ["\n".join(header)]

    for f in files:
        parts.append("")
        parts.append(f"-- " + "=" * 72)
        parts.append(f"-- BEGIN {f.name}")
        parts.append(f"-- " + "=" * 72)
        parts.append("")
        parts.append(f.read_text(encoding="utf-8").rstrip())
        parts.append("")
        parts.append(f"-- " + "=" * 72)
        parts.append(f"-- END {f.name}")
        parts.append(f"-- " + "=" * 72)

    return "\n".join(parts) + "\n"


def process_form(form_dir: Path) -> int:
    sql_dir = form_dir / "sql-migrations"
    if not sql_dir.is_dir():
        return 0

    files = numbered_sql_files(sql_dir)
    if not files:
        return 0

    out = sql_dir / COMBINED_NAME
    out.write_text(render_combined(form_dir.name, files), encoding="utf-8")
    return len(files)


def main():
    total_forms = 0
    total_files = 0
    form_dirs = sorted(d for d in FORMS_DIR.iterdir() if d.is_dir())
    for form_dir in form_dirs:
        if not (form_dir / "sql-migrations").is_dir():
            continue
        n = process_form(form_dir)
        if n > 0:
            total_forms += 1
            total_files += n
            print(f"  {form_dir.name}: combined {n} files -> sql-migrations/{COMBINED_NAME}")

    print(f"\nTotal: {total_forms} forms, {total_files} source files combined")


if __name__ == "__main__":
    main()
